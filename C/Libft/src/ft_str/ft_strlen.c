#include "libft.h"
#include <unistd.h>
#include <string.h>
#include <stdlib.h>
#include <stddef.h>

size_t	ft_strlen(const char *str)
{
	int	i;

	i = 0;
	while (str[i])
		i++;
	return (i);
}	
